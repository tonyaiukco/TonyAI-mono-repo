// Calculate target progress
export function calculateTargetProgress(target: EmissionsTarget, currentEmissions: number): TargetProgress {
  const varianceToTarget = currentEmissions - target.targetEmissions;
  const totalReductionNeeded = target.baselineEmissions - target.targetEmissions;
  const actualReduction = target.baselineEmissions - currentEmissions;
  const progressPercent = totalReductionNeeded > 0 ? (actualReduction / totalReductionNeeded) * 100 : 0;
  
  let status: TargetStatus = 'on_track';
  if (progressPercent < 50) status = 'off_track';
  else if (progressPercent < 80) status = 'at_risk';
  
  return {
    targetId: target.id,
    currentEmissions,
    targetEmissions: target.targetEmissions,
    baselineEmissions: target.baselineEmissions,
    varianceToTarget,
    progressPercent: Math.min(100, Math.max(0, progressPercent)),
    status,
  };
}

