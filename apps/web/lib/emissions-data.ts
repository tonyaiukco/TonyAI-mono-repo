// Helper function to calculate scope totals from records
export function calculateScopeTotals(records: EmissionsRecord[]) {
  const scope1 = records.filter(r => r.scope === 1).reduce((sum, r) => sum + r.tCo2e, 0);
  const scope2 = records.filter(r => r.scope === 2).reduce((sum, r) => sum + r.tCo2e, 0);
  const scope3 = records.filter(r => r.scope === 3).reduce((sum, r) => sum + r.tCo2e, 0);
  return { scope1, scope2, scope3, total: scope1 + scope2 + scope3 };
}

// Get top contributors by subsidiary or category
export function getTopContributors(records: EmissionsRecord[], by: 'subsidiary' | 'category', limit: number = 5) {
  const grouped = records.reduce((acc, record) => {
    const key = by === 'subsidiary' ? record.subsidiary : record.category;
    acc[key] = (acc[key] || 0) + record.tCo2e;
    return acc;
  }, {} as Record<string, number>);

  return Object.entries(grouped)
    .map(([name, value]) => ({ name, value }))
    .sort((a, b) => b.value - a.value)
    .slice(0, limit);
}

